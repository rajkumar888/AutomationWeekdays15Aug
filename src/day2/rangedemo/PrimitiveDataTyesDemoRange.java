package day2.rangedemo;

public class PrimitiveDataTyesDemoRange {

	static int intx=2147483647;
	static byte bytex= (byte) 129;
	static short shortx=(short) 32768;
	static long longx=15000000000L;;
	
	static float floata=5.75f;
	static double doublea=19.99;
	
	static char charA;
	
	static boolean booleanA=true;
	
	
	static String name="hello world";
	
	public static void main(String[] args) {
		
		
		System.out.println(intx);
		System.out.println(bytex);
		System.out.println(shortx);
		System.out.println(longx);
		System.out.println(".........................");
		System.out.println(floata);
		System.out.println(doublea);
		System.out.println(".........................");
		System.out.println(charA);
		System.out.println(".........................");
		System.out.println(booleanA);
		
		System.out.println(".........................");
		System.out.println(name);
		
		
	}

}

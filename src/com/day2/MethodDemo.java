package com.day2;

public class MethodDemo {
	
	
	public int getAddition(int val1, int val2){
		return val1+val2;
	}
	
	public int getSubraction(int val1, int val2){
		return val1-val2;
	}
	
	public int getProduct(int val1, int val2){
		return val1*val2;
	}
	
	public int getDivQuotient(int val1, int val2){
		return val1/val2;
	}
	
	public int getDivRemainder(int val1, int val2){
		return val1%val2;
	}

}

package javaoperators;

public class ArithmeticOperatorDemoTest {
	
	
	
	public static void main(String[] args) {
		
		
		int x=11;
		
		int y=2;
		
		
		System.out.println("value of x "+x);
		System.out.println("value of y "+y);
		
		
		System.out.println("Addition of given numbers "+(x+y));
		System.out.println("subtraction of given numbers "+(x-y));
		System.out.println("quotient of given numbers "+(x/y));
		System.out.println("mulitiplication of given numbers "+(x*y));
		System.out.println("remainder of given numbers "+(x%y));
		
		
	}

}

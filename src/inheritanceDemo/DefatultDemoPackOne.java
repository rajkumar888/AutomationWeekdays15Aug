package inheritanceDemo;

public class DefatultDemoPackOne {
	
	
	public int publicX=100;
	
	protected int protectedX=200;
	
	int defaultX  = 300;
	
	private int privateX = 800;
	
	
	public int getpublicX(){
		return publicX;
	}
	
	protected int getprotectedX(){
		return protectedX;
	}
	
	int getdefaultX(){
		return defaultX;
	}
	
	
	private int getPrivateX(){
		return privateX;
	}
	

}
